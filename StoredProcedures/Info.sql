/* Хранимая процедура - именованная последовательность выражений 
и других конструкций языка SQL.
Функция - именованная последовательность 
Ключевое отличие между функцией и ХП состоит в том,
что хранимые процедуры поддерживают транзакции, а функции - нет.
Транзакция - последовательность действий, которая вносит данные в БД.

% - любое количество любых печатных символов
_  - единичный символ
[] - один символ из набора символов в квадратных скобках
[^] - символ кроме символов в скобках
 */